class.purify = function (strOrig)
    local strUpper = string.upper(strOrig)

    for k,fbdTxt in ipairs(class.txtDef) do
        local s, e
        repeat
            s, e = string.find(strUpper, fbdTxt)
            if s and e then
                strOrig = string.sub(strOrig, 1, s-1) .. "*" .. string.sub(strOrig, e+1)
                strUpper = string.upper(strOrig)
            end
        until s == nil
    end

    return strOrig
end

